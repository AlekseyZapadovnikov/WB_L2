package main

import (
	"fmt"
	"sort"
	"strings"
)

// sortString принимает строку и возвращает новую строку с отсортированными по возрастанию рунами (символами).
// Это позволяет получить "каноническое" представление слова для поиска анаграмм.
// Например, для "пятка" и "тяпка" результатом будет "акптя".
func sortString(s string) string {
	// Преобразуем строку в срез рун для корректной работы с Unicode (кириллицей)
	runes := []rune(s)
	
	// Сортируем срез рун
	sort.Slice(runes, func(i, j int) bool {
		return runes[i] < runes[j]
	})
	
	// Преобразуем отсортированный срез рун обратно в строку
	return string(runes)
}

// findAnagrams находит все множества анаграмм в заданном словаре.
//
// words: срез строк для поиска анаграмм.
//
// Возвращает map, где:
// - ключ: первое встреченное слово из множества анаграмм.
// - значение: отсортированный по алфавиту срез всех слов-анаграмм этого множества.
//
// Множества из одного элемента игнорируются.
func findAnagrams(words []string) map[string][]string {
	// groups будет хранить группы анаграмм по их каноническому ключу.
	// ключ: отсортированная строка ("акптя")
	// значение: срез оригинальных слов (в нижнем регистре), которые соответствуют ключу
	groups := make(map[string][]string)

	// Шаг 1: Группировка слов по каноническому ключу.
	for _, word := range words {
		// Приводим слово к нижнему регистру согласно требованию
		lowerWord := strings.ToLower(word)
		
		// Получаем канонический ключ (отсортированную строку)
		key := sortString(lowerWord)
		
		// Добавляем слово в соответствующую группу
		groups[key] = append(groups[key], lowerWord)
	}

	// result — это финальная карта для вывода.
	result := make(map[string][]string)

	// Шаг 2: Формирование результирующей карты.
	for _, anagramSet := range groups {
		// Игнорируем множества, состоящие из одного слова
		if len(anagramSet) <= 1 {
			continue
		}
		
		// Ключом является первое слово, которое мы встретили для этой группы анаграмм.
		// Так как мы итерируемся по исходному срезу по порядку, anagramSet[0]
		// и будет тем самым первым встреченным словом.
		key := anagramSet[0]
		
		// Сортируем срез анаграмм по алфавиту согласно требованию
		sort.Strings(anagramSet)
		
		// Записываем отсортированный срез в результирующую карту
		result[key] = anagramSet
	}

	return result
}

func main() {
	// Пример из задания
	dictionary := []string{"пятак", "пятка", "тяпка", "листок", "слиток", "столик", "стол"}

	anagrams := findAnagrams(dictionary)

	// Вывод результата для проверки
	// (порядок вывода ключей в map не гарантирован)
	for key, group := range anagrams {
		fmt.Printf("%q: %v\n", key, group)
	}

	fmt.Println("---")

	// Другой пример для проверки
	dictionary2 := []string{"Виза", "Залив", "Ливень", "Зима", "низ", "валик", "виза"}
	anagrams2 := findAnagrams(dictionary2)
	for key, group := range anagrams2 {
		fmt.Printf("%q: %v\n", key, group)
	}
}